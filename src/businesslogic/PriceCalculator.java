package businesslogic;

public class PriceCalculator {

    public double suma(double cena1, double cena2, double cena3, double cena4) {
        return cena1 + cena2 + cena3 + cena4;
    }

    public double srednia(double cena1, double cena2, double cena3, double cena4) {
        return (cena1 + cena2 + cena3 + cena4) / 4;
    }
}
